public class SequenciaEscape {
    
    public static void main(String[] args) {
        
        // Olá, mundo!
        
        System.out.println("\"Olá, mundo!\""); // Salto de linha
        System.out.print("\"Olá, mundo!\""); // Sem salto de linha
        
        System.out.println("");
        
        System.out.println("1\\4");
    }
}